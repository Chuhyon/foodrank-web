def is_menu_keyword(keyword: str) -> bool:
    """음식 메뉴 관련 키워드인지 판별"""
    kw = keyword.strip()
    return any(indicator in kw for indicator in FOOD_INDICATORS)


def is_brand_keyword(keyword: str) -> bool:
    """외식 브랜드 관련 키워드인지 판별"""
    kw = keyword.strip()
    return any(brand.lower() in kw.lower() for brand in BRAND_KEYWORDS)


def is_food_keyword(keyword: str) -> bool:
    """음식(메뉴 또는 브랜드) 관련 키워드인지 판별"""
    return is_menu_keyword(keyword) or is_brand_keyword(keyword)


def extract_menu_keywords(text: str) -> list:
    """텍스트에서 메뉴 키워드 추출"""
    return [kw for kw in MENU_KEYWORDS if kw in text]


def extract_brand_keywords(text: str) -> list:
    """텍스트에서 브랜드 키워드 추출"""
    return [kw for kw in BRAND_KEYWORDS if kw in text]


def extract_food_term(phrase: str) -> str | None:
    """
    복합 구절에서 핵심 음식 키워드 추출
    예) "광양 맛집" → None (음식 단어 없음)
        "마라탕 맛집 추천" → "마라탕"
        "빽다방 신메뉴" → "빽다방"
    """
    for kw in BRAND_KEYWORDS:
        if kw in phrase:
            return kw
    for kw in MENU_KEYWORDS:
        if kw in phrase:
            return kw
    return None
